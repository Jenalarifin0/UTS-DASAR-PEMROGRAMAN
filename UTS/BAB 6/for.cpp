#include <iostream>
using namespace std;

int main ()
{
    // pengulangan menaik
    cout << "PENGULANGAN MENARIK" << endl;
    for (int C=0; C<10; C++)
    {
        cout << C+1 << endl;
    }
    cout << "\n";
    //pengulngan menurun
    cout << "PENGULANAN MENURUN" << endl;
    for (int D=10; D>0; D--)
    {
        cout << D << endl;
    }
    return 0;
}
